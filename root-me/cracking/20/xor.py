def unsigned8(x): 
    if(x < 0):
        return x + 2**8
    else:
        return x

# 0xf1 = unsigned8((0xf5 ^ k) - 0xaa)) ^  unsigned((0x0f ^ k) - 0xaa) ^ unsigned((0xf1 ^ k) - 0xaa)     


def modK(k, x):
    return unsigned8((x ^ k) - 0xaa) % 2**8

